using System;

class DeclaracaoVariaveis
{
	static void Main()
	{
		/*
		Declaração de variável
	    */		
		int a;
		
		// Inicialização da Variável
		a = 22;
		Console.WriteLine(a);
		
		//Reatribuição de valor na variável
		a = 56;
		Console.WriteLine(a);
		
		//Outras formas de declaração de variáveis
		
		//Declarando e incializando a variável
		int b = 925;
		Console.WriteLine(b);	
		
		int y = 10, x = 20;
		int z = y + x;
		Console.WriteLine(z);
		
		
	}	
}